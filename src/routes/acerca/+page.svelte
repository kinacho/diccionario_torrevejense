        <h1 class="text-4xl font-bold text-torrevieja-blue-900 border-b border-torrevieja-blue-100 pb-4 mb-8">
            Diccionario Torrevejense Interactivo
        </h1>
        
        <p>
            Este proyecto nace con la vocación de preservar y difundir la riqueza léxica de Torrevieja. 
            El habla torrevejense es un crisol de influencias marinas, salineras y agrícolas, 
            con profundas raíces en el murciano y el valenciano, pero con una personalidad propia 
            forjada en la singularidad de su geografía y su historia.
        </p>

        <h2 class="text-2xl font-bold mt-12 mb-6 text-slate-800">Sobre el Corpus</h2>
        <p>
            La base de este diccionario digital es una recopilación exhaustiva que incluye más de 1.300 entradas, 
            documentando no solo términos aislados sino también locuciones, frases hechas y etimologías 
            que explican el origen de cada palabra.
        </p>

        <h2 class="text-2xl font-bold mt-12 mb-6 text-slate-800">Funcionalidades</h2>
        <ul>
            <li><strong>Buscador Inteligente:</strong> Con filtros avanzados y búsqueda aproximada.</li>
            <li><strong>Palabra del Día:</strong> Cada día, una nueva voz del corpus para redescubrir.</li>
            <li><strong>Visualización de Datos:</strong> Estadísticas en tiempo real sobre la distribución del léxico.</li>
            <li><strong>Portabilidad:</strong> Funciona offline y es totalmente adaptable a móviles y tablets.</li>
        </ul>

        <div class="mt-20 p-8 bg-torrevieja-blue-50 rounded-3xl not-prose border border-torrevieja-blue-100">
